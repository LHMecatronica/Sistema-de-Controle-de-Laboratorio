package br.com.lhmecatronica.persistencia;

public class ComponenteEncapsulamento {

	int idEncapuslamento;
	String encapsulamentoTipo;
	String encapsulamentoFoto;
	String encapsulamentoDescricao;

	public int getIdEncapuslamento() {
		return idEncapuslamento;
	}

	public void setIdEncapuslamento(int idEncapuslamento) {
		this.idEncapuslamento = idEncapuslamento;
	}

	public String getEncapsulamentoTipo() {
		return encapsulamentoTipo;
	}

	public void setEncapsulamentoTipo(String encapsulamentoTipo) {
		this.encapsulamentoTipo = encapsulamentoTipo;
	}

	public String getEncapsulamentoFoto() {
		return encapsulamentoFoto;
	}

	public void setEncapsulamentoFoto(String encapsulamentoFoto) {
		this.encapsulamentoFoto = encapsulamentoFoto;
	}

	public String getEncapsulamentoDescricao() {
		return encapsulamentoDescricao;
	}

	public void setEncapsulamentoDescricao(String encapsulamentoDescricao) {
		this.encapsulamentoDescricao = encapsulamentoDescricao;
	}

}
